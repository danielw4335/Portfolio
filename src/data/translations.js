export const getTranslation = (key, language = 'en') => {
  const keys = key.split('.');
  let translation = translations[language];

  for (const k of keys) {
    if (translation[k] !== undefined) {
      translation = translation[k];
    } else {
      return key; // Return the original key if not found
    }
  }
  return translation;
};